def triangle(base,height):
    """
    return area of triangle
    """
    area = 0.5*base*height
    return area
# print(triangle(20,50))

def square(length):
    """
    return area of square
    """
    area = length * length
    return area
# print(square(9))

def rectangle(length,width):
    """
    return area of square
    """
    area = length * width
    return area
# print(rectangle(2,3))


